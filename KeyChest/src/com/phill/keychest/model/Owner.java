package com.phill.keychest.model;

public class Owner {
	
	private int id;
	private String name;
	
	public void setID(final int id) {
		this.id = id;
	}
	
	public void setName(final String name) {
		this.name = name;
	}
	
	public int getID() {
		return this.id;
	}
	
	public String getName() {
		return this.name;
	}

}
